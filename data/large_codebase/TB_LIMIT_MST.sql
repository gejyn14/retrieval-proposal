CREATE TABLE TB_LIMIT_MST (
    LIMIT_TP    VARCHAR2(10)  NOT NULL,
    LIMIT_CD    VARCHAR2(20)  NOT NULL,
    LIMIT_AMT   NUMBER(15,2),
    USED_AMT    NUMBER(15,2)  DEFAULT 0,
