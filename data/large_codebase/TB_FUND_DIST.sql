CREATE TABLE TB_FUND_DIST (
    FUND_CD     VARCHAR2(12)  NOT NULL,
    DIST_DT     VARCHAR2(8)   NOT NULL,
    DIST_TP     VARCHAR2(4),
    DIST_AMT    NUMBER(12,4),
    BASE_DT     VARCHAR2(8),
    PAY_DT      VARCHAR2(8),
