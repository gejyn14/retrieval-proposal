CREATE TABLE TB_TX_HIST (
    TX_NO       NUMBER(15)    NOT NULL,
    TX_DT       VARCHAR2(8),
    TX_TM       VARCHAR2(6),
    SVC_NM      VARCHAR2(20),
    ACCT_NO     VARCHAR2(11),
    TX_TP       VARCHAR2(10),
    TX_AMT      NUMBER(15,2),
    TX_DESC     VARCHAR2(200),
    RESULT_CD   VARCHAR2(4),
    ERR_MSG     VARCHAR2(200),
    USER_ID     VARCHAR2(20),
    CONSTRAINT PK_TX_HIST PRIMARY KEY (TX_NO)
);
COMMENT ON TABLE TB_TX_HIST IS '거래 이력';
