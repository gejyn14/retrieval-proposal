CREATE TABLE TB_RATE_MST (
    RATE_TP     VARCHAR2(10)  NOT NULL,
    BASE_DT     VARCHAR2(8)   NOT NULL,
    RATE_VAL    NUMBER(12,6),
    CUR_CD      VARCHAR2(3),
    USE_YN      CHAR(1)       DEFAULT 'Y',
