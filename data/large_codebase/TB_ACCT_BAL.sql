    CASH_BAL    NUMBER(15,2)  DEFAULT 0,
    AVAIL_AMT   NUMBER(15,2)  DEFAULT 0,
    D1_AMT      NUMBER(15,2)  DEFAULT 0,
    D2_AMT      NUMBER(15,2)  DEFAULT 0,
