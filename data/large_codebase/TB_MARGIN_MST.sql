CREATE TABLE TB_MARGIN_MST (
    STK_CD      VARCHAR2(7)   NOT NULL,
    MARGIN_GRP  VARCHAR2(2),
    MARGIN_RATE NUMBER(5,2),
    SUBST_RATE  NUMBER(5,2),
    EFF_DT      VARCHAR2(8),
    USE_YN      CHAR(1)       DEFAULT 'Y',
