CREATE TABLE TB_ORD_MST (
    ORD_NO      NUMBER(12)    NOT NULL,
    ACCT_NO     VARCHAR2(11)  NOT NULL,
    STK_CD      VARCHAR2(7),
    ORD_TP      VARCHAR2(4),
    PRICE       NUMBER(12,2),
    QTY         NUMBER(10),
    ORD_DT      VARCHAR2(8),
    ORD_ST      VARCHAR2(2),
    CONSTRAINT PK_ORD_MST PRIMARY KEY (ORD_NO)
);
COMMENT ON TABLE TB_ORD_MST IS '주문 마스터';
